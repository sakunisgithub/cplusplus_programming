#include<iostream>

using namespace std;

int main()
{
    int a, b;

    cout << "Enter the first number = ";
    cin >> a;

    cout << "Enter the second number = ";
    cin >> b;

    cout << "The sum of the two numbers is " << a+b;

    return 0;
}

// << is called the insertion operator
// >> is called the extraction operator